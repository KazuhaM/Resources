primid <- c(0.59, 0.51, 0.37, 0.62, 0.63, 0.54, 0.57, 0.88, 0.51, 0.43)
contrl <- c(0.31, 0.40, 0.41, 0.35, 0.26, 0.55, 0.41, 0.43, 0.45)
boxplot(primid, contrl, names=c("プリミドン", "対照群"))
#帰無仮説： プリミドン群の母平均 = 対照群の母平均
#対立仮説： プリミドン群の母平均 > 対照群の母平均
t.test(x = primid, y = contrl, alternative = "greater")
#有意確率は0.002634で、有意水準より小さく、帰無仮説を棄却し、対立仮説を採択する。
#プリミドン投与によりベメグライドの最小有効量が増えた。

