h21 <- c(42, 85, 42, 83, 67, 42, 57, 48, 66, 30)
h22 <- c(24, 49, 23, 63, 32, 17, 50, 53, 70, 40)
boxplot(h21 - h22, ylab="点数の差（平成21年度問題-平成22年度問題）")
