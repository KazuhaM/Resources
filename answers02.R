#4
#データの母集団の母平均をmuとすると、
#帰無仮説H0: mu = 170
#対立仮説H1: mu != 170 (!=は等しくないの意味で使っています）
data <- c(162.8, 162.2, 168.7, 169.9, 166.2, 166.4, 
165.9, 168.0, 170.1, 173.9, 171.7, 169.3, 167.2)
t.test(x = data, mu = 170)
#有意確率は0.03779なので、帰無仮説を棄却し、対立仮説を採択する。データは母平均=170の母集団からの標本と言えない。

