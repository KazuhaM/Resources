before <- c(53.0,	50.2,	59.4,	61.9)
after <- c(51.2,	48.7,	53.5,	56.1)
boxplot(after - before, ylim=c(-6, 1), ylab="ダイエット後の体重−ダイエット前の体重")
abline(h=0, lty=2)
#帰無仮説： ダイエット後−ダイエット前の体重差の母平均=0
#対立仮説： ダイエット後−ダイエット前の体重差の母平均 < 0
t.test(after, before, paired=TRUE, alternative = "less")
#有意確率は0.02689なので、帰無仮説を棄却し、対立仮説を採択する。
#リンゴダイエットは効果があると言える。

