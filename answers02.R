#帰無仮説：　平成21年度−平成22年度の点数の差の母平均 = 0
#対立仮説：　平成21年度−平成22年度の点数の差の母平均 != 0
t.test(x = h21, y = h22, paired=TRUE)
#有意確率は0.02392なので、帰無仮説を棄却できる。
#平成21年度と22年度の問題の難易度には違いがある。








